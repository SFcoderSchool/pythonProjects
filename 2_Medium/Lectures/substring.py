# Start with some String
phrase = "I like to code!"

# Use indexing to get specific characters from the String
# indexes start at 0 up to the length-1

# first character
print(phrase[0])
# second character
print(phrase[1])

# Substring to get a chunk of the String
print(phrase[2:6:1])

# Find out how long the String is
print(len(phrase))

