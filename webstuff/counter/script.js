let num = 0;

function minus() {
  num = num - 1;
  document.getElementById("num").innerHTML = num;
}

function plus() {
  num = num + 1;
  document.getElementById("num").innerHTML = num;
}

function minus2() {
  num = num - 2;
  document.getElementById("num").innerHTML = num;
}

function plus2() {
  num = num + 2;
  document.getElementById("num").innerHTML = num;
}

function reset() {
  num = 0;
  document.getElementById("num").innerHTML = num;
}
